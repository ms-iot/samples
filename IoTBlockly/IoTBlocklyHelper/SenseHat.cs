        private async Task InitSenseHat()
        {
            if (senseHat == null)
            {
                senseHat = await SenseHatFactory.Singleton.GetSenseHat();
            }
        }

        private void ReadAccelerometor()
        {
            if (senseHat == null) { return; }
            if (!senseHat.Sensors.ImuSensor.Update()) { return; }
            if (!senseHat.Sensors.Acceleration.HasValue) { return; }
            accel = senseHat.Sensors.Acceleration.Value;
        }

