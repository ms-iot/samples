        // notice how we keep the "hat" static so that it can be reused when we stop/restart/modify the js script
        private static ISenseHat senseHat;
        private static SingleColorFont font;
        private readonly ManualResetEventSlim waitEvent = new ManualResetEventSlim(false);

        private Vector3 accel;

        public SenseHat()
        {
            InitSenseHat().Wait();

            // Recreate the font from the serialized bytes.
            font = SingleColorFont.Deserialize(FontBytes);
        }

        public void Clear(bool update)
        {
            if (senseHat == null) { return; }
            senseHat.Display.Clear();
            if (update)
            {
                senseHat.Display.Update();
            }
        }

        public void DrawLedMatrix(string matrix, Color foreground, int offsetX, int offsetY, bool update)
        {
            if (senseHat == null) { return; }
            if (offsetX > 7 || offsetY > 7) { return; }
            int x = offsetX;
            int y = offsetY;
            bool somethingGotUpdated = false;
            foreach (var c in matrix)
            {
                switch (c)
                {
                    case '0':
                        ++x;
                        break;
                    case '1':
                        if (x >= 0 && x < 8 && y >=0 && y < 8)
                        {
                            senseHat.Display.Screen[x, y] = foreground;
                            somethingGotUpdated = true;
                        }
                        ++x;
                        break;
                    case '|':
                        x = offsetX;
                        ++y;
                        break;
                    default:
                        ++x;
                        break;
                }
                if (y > 7) { break; }
            }
            if (somethingGotUpdated && update)
            {
                senseHat.Display.Update();
            }
        }

        public int GetAccel(int axis)
        {
            if (senseHat == null) { return 0; }
            ReadAccelerometor();
            double val = 0;
            switch (axis)
            {
                case 0:
                    val = accel.X;
                    break;
                case 1:
                    val = accel.Y;
                    break;
                case 2:
                    val = accel.Z;
                    break;
            }
            return (int)(val * 1000);
        }

        public void Print(string message, Color background, Color foreground)
        {
            if (senseHat == null) { return; }

            // Get the characters to scroll.
            IEnumerable<SingleColorCharacter> characters = font.GetChars(message);

            // Create the character renderer.
            SingleColorCharacterRenderer characterRenderer = new SingleColorCharacterRenderer(_ => foreground);

            // Create the text scroller.
            var textScroller = new TextScroller<SingleColorCharacter>(
                senseHat.Display,
                characterRenderer,
                characters);

            while (textScroller.Step())
            {
                // Draw the background.
                senseHat.Display.Fill(background);

                // Draw the scroll text.
                textScroller.Render();

                // Update the physical display.
                senseHat.Display.Update();

                // Pause for a short while.
                waitEvent.Wait(TimeSpan.FromMilliseconds(50));
            }

        }

